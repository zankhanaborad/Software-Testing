package Basic;

public class Typecasting {
public static void main(String[] args) {
	int i=1;
	double d=1;
	
	System.out.println("i : "+i);
	System.out.println("d : "+d);
	
	int j = (int)3.14;
	System.out.println(j);
	
	
	int a=10;
	int b=20;
	double c = (double)a / (double)b;
	System.out.println("c:"+c);
	System.out.println("a :"+a);
			
}
}
